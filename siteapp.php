      <h2>Responsive Websites vs Mobile Apps</h2>
      <br>
        <p>Since the beginning of our well known “www” in the early ’90s, our website standard and design have come a long way.
          The introduction of JavaScript has changed the user interface to create a more responsive experience.
          The functionalities of JavaScript have expanded to different device sizes with the addition of a media query.
          There are now more ways of testing browser support by JQuery Mobile and Modernizr.
          These changes have made our web design to perform without the limitations of being constricted by our device sizes.</p>
      <br>
        <p>In recent studies made in 2019 by Pew Research Center, 81% of U.S. adults own a smartphone and 74% of U.S adults own a desktop or laptop device[2].
          As more and more of our tasks can be completed online, our use of mobile devices is increasing and functions on our applications are becoming better than ever.
          According to Stat counter, 50.48% of traffic is mobile and 46.51% is desktop.
          CleverTap reported there is a mobile application conversion of a 100%-300% high rate than efficiently optimized websites.</p>
      <br>

        <p>In 2020, we have over 2.7 billion mobile device users across the globe and the mobile app industry is thriving and it shows no sign of slowing down[1].
           Marketers have realized that it is more cost-effective to advertise on mobile devices compared to desktops[5].
           The design for mobile devices has become more of a rule rather than an exception.
           With the user statistics in mind, there are pros and cons to using mobile apps vs. responsive websites. </p>
      <br>
        <p>Websites do not require installations or downloading and most websites are free to use.
           Also, operation systems are not an important factor when using a website.
           Updating, supporting, and bug fixing can be made easier on websites compared to mobile apps because users do not need to install newer versions of the applications.
           The changes made to a website are cheaper and easier for an owner because there is only one website and not multiple versions. </p>
    <br>
        <p>Social media has made it easy to connect with others and the use of mobile apps has made it a more personalized experience.
           Unlike websites, some mobile apps can operate without a internet connection.
           Notifications, reminders, locations, scanning, microphone, camera, and more features offered by mobile apps create a more engaging experience for users.
           Mobile apps let users set up their preferences and offer custom recommendations based on their tracking abilities of user statistics.
           These flexibilities create custom and tailored experience for users to easily become accustomed and create a higher chance of conversion from websites to apps[3].</p>
    <br>
    <br>
      <p>In conclusion, our use of mobile apps is increasing due to the convenience of our portable devices and their customizable features.
         Our use of a responsive website still offers a more versatile solution to the general and with media-query can perform on variety of devices.
         Websites and mobile apps have their pros and cons and developers need to understand both platforms.
         “Whichever you decide, the principal goals to keep in mind are desired functionality, adaptability to mobile, and the overall user experience”[4].</p>

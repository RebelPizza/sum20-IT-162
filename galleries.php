      <h2>Galleries</h2>
      <br>
                <p>According to research by Times.com, the average user spends 15 seconds on a website.
       Ever since we could interact with our mobile apps and responsive, most of us have less patience to read a whole article or look through the whole website.
       Unless visitors find content to be interesting and useful on your website, they will look elsewhere for a better result.
       As humans, we are visual creatures because much of our sensory cortex is devoted to vision.
        We have historical museums and art galleries for many different purposes and the equivalent of those on a website is galleries.</p>
        <br>
                <p>As the common saying goes “a picture tells a thousand words”, images are an effective way to engaging an audience and even more so when they are responsive.
         Galleries are an efficient method of introducing and informing your audience of your website.
         They provide a great layout, help your websites look more attractive, and will give your audience a choice to connect with your content better.
         There are different types of galleries such as sliders, image grids, cover flows and there are more categories within these types like 2D and 3D.
          With endless creative possibilities, it all depends on how you want to present your layout and your message.</p>
          <br>

                <p>Developers have multiple options to choose from when they are creating galleries on their websites.
         Galleries are commonly composed of HTML, CSS, JavaScript, and/or JQuery.
         HTML provides the structure, CSS adds design properties, JavaScript allows the website to be interactive and JQuery offers a library system.
         The combination of these programming languages can help us develop a cross-platform gallery.
         Some developers choose to use templates and bootstraps from online resources but it would be a great challenge if you decide to create a gallery from scratch.</p>
